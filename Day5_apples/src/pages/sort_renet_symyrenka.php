    <h2>Сорт: Ренет Симиренка (Ренет Семеренка)</h2>
    <div style="border: 1px solid #ccc; padding: 10px; margin-bottom: 15px;">
        <p><strong>Смак:</strong> Винно-кислуватий, дуже освіжаючий, з легкою пряною ноткою. М'якоть зеленувато-біла, дуже хрустка.</p>
        <p><strong>Особливості:</strong> Класичний зимовий сорт, який може зберігатися до весни. Характерною ознакою є яскраво-зелений колір шкірки з невеликими світлими цятками.</p>
