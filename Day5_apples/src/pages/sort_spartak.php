    <h2>Сорт: Спартак</h2>
    <div style="border: 1px solid #ccc; padding: 10px; margin-bottom: 15px;">
        <p><strong>Смак:</strong> Дуже солодкий, майже без кислинки, з медовими нотками.</p>
        <p><strong>Особливості:</strong> Це пізній осінній сорт. Яблука середнього розміру, часто мають темно-червоний або бордовий колір. Відмінно підходять для зимового зберігання завдяки щільній м'якоті.</p>
