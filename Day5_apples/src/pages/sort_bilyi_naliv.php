    <h2>Сорт: Білий налив (Налив Білий)</h2>
    <div style="border: 1px solid #ccc; padding: 10px; margin-bottom: 15px;">
        <p><strong>Смак:</strong> Кисло-солодкий, дуже освіжаючий. М'якоть пухка, соковита, з характерним легким ароматом.</p>
        <p><strong>Особливості:</strong> Це один із найбільш ранніх літніх сортів. Яблука світло-жовтого кольору, майже прозорі. Не підлягають тривалому зберіганню, ідеальні для споживання одразу або для приготування варення.</p>
