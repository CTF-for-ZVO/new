    <h2>Сорт: Слава переможцям</h2>
    <div style="border: 1px solid #ccc; padding: 10px; margin-bottom: 15px;">
        <p><strong>Смак:</strong> Гармонійний, кисло-солодкий, винний, з приємним ніжним ароматом.</p>
        <p><strong>Особливості:</strong> Осінній сорт, який відомий своєю високою врожайністю. Плоди великі, мають яскраво-червоний смугастий рум'янець. Добре зберігаються, не втрачаючи соковитості.</p>
